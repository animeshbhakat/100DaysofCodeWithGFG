class Solution{
public:	
	int maxSubstring(string S)
	{
	    int ani = 0, ans = -1;
        for (auto c : S) {
            if (c == '0')ani++;
            else ani--;
            ans = max(ans, ani);
            if (ani < 0)ani = 0;
        }
        return ans;
	}
};

